    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText().toUpperCase();
        String cpf = txtCpf.getText();
        String telefone = txtTelefone.getText();
        String celular = txtCelular.getText();
        String endereco = txtEndereco.getText().toUpperCase();

        if (nome.isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo nome é obrigatório", "Alerta", JOptionPane.ERROR_MESSAGE);
        } else {
            clientes = new Clientes();
            cdao = new ClientesDAO();

            clientes.setNome(nome);
            clientes.setCpf(cpf);
            clientes.setTelefone(telefone);
            clientes.setCelular(celular);
            clientes.setEndereco(endereco);
            cdao.salvar(clientes);

            // método limpar os campos(Clear)
            this.Clear();
            this.listaClientes("");
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
            btnSalvar.setVisible(false);
            
            desabilitaCampostxt();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (txtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo nome é obrigatório", "Alerta", JOptionPane.ERROR_MESSAGE);
        } else {
            clientes = new Clientes();
            cdao = new ClientesDAO();

            clientes.setCodigo(Integer.parseInt(lbCodigo.getText()));
            clientes.setNome(txtNome.getText().toUpperCase());
            clientes.setCpf(txtCpf.getText());
            clientes.setTelefone(txtTelefone.getText());
            clientes.setCelular(txtCelular.getText());
            clientes.setEndereco(txtEndereco.getText().toUpperCase());

            cdao.buscarPorCodigo(clientes);
            cdao.editar(clientes);
            this.listaClientes("");
            this.Clear();
            JOptionPane.showMessageDialog(null, "Editado com sucesso!");
            btnEditar.setVisible(false);
            btnSalvar.setVisible(false);
            
            desabilitaCampostxt();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

