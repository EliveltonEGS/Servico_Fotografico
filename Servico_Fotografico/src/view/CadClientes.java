    //carrega tabela de clientes
    private void listaClientes(String nome) {
        DefaultTableModel modeloTabela = new DefaultTableModel();

        modeloTabela.addColumn("ID");
        modeloTabela.addColumn("NOME");
        modeloTabela.addColumn("CPF");
        modeloTabela.addColumn("TELEFONE");
        modeloTabela.addColumn("CELULAR");
        modeloTabela.addColumn("ENDEREÇO");

        cdao = new ClientesDAO();
        for (Clientes c : cdao.pesquisarPorNome(nome)) {
            modeloTabela.addRow(new Object[]{c.getCodigo(), c.getNome(), c.getCpf(), c.getTelefone(), c.getCelular(), c.getEndereco()});
        }

        tbClientes.setModel(modeloTabela);
    }

