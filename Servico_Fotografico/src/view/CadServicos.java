    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        try {
            servicos = new Servicos();
            clientes = new Clientes();
            sdao = new ServicosDAO();

            //formata data para salvar no banco
            String dia = txtData.getText().substring(0, 2);
            String mes = txtData.getText().substring(3, 5);
            String ano = txtData.getText().substring(6);

            String dataSalva = ano + "-" + mes + "-" + dia;

            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-ddd");
            Date data = formato.parse(dataSalva);

            clientes.setCodigo(Integer.parseInt(txtCodigoCliente.getText()));

            servicos.setDescricao(txtDescricao.getText().toUpperCase());
            servicos.setNumero(Integer.parseInt(txtNumero.getText()));
            servicos.setValor(Double.parseDouble(txtValor.getText().replace(",", ".")));
            servicos.setClientes(clientes);
            servicos.setData(data);
            sdao.salvar(servicos);

            this.listaServicos("");
            
            this.Clear();
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
            
            desabilitaCampos();

            //Imprime recibo
            Connection con = ConnectionFactory.conectar();

            String caminho = "C:/relatorios/comprovante.jasper";
            //String caminho = "C:/Users/Elivelton Silva/Documents/NetBeansProjects/Rodrigo_Foto/src/relatorio_temp/comprovante.jasper";
            try {
                JasperPrint print = JasperFillManager.fillReport(caminho, null, con);

                JasperViewer jv = new JasperViewer(print, false);
                jv.setVisible(true); //chama o formulario para visualização
                jv.toFront(); //set o formulario a frente da aplicação
                btnSalvar.setVisible(false);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao chamar o relatório!\nErro:" + ex);
                ex.printStackTrace();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Valor inválido para Número ou Valor!/Preencha todos os campos!", "Atenção", JOptionPane.ERROR_MESSAGE);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Informe a Data do Serviço!", "Atenção", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            servicos = new Servicos();
            clientes = new Clientes();
            sdao = new ServicosDAO();

            //formata data para salvar no banco
            String dia = txtData.getText().substring(0, 2);
            String mes = txtData.getText().substring(3, 5);
            String ano = txtData.getText().substring(6);

            String dataSalva = ano + "-" + mes + "-" + dia;

            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-ddd");
            Date data = formato.parse(dataSalva);

            clientes.setCodigo(Integer.parseInt(txtCodigoCliente.getText()));

            servicos.setCodigo(Integer.parseInt(lbcodigo.getText()));
            servicos.setDescricao(txtDescricao.getText().toUpperCase());
            servicos.setNumero(Integer.parseInt(txtNumero.getText()));
            servicos.setValor(Double.parseDouble(txtValor.getText().replace(",", ".")));
            servicos.setClientes(clientes);
            servicos.setData(data);

            sdao.editar(servicos);

            this.listaServicos("");
            this.Clear();
            JOptionPane.showMessageDialog(null, "Editado com sucesso!");
            btnEditar.setVisible(false);
            btnSalvar.setVisible(false);
            btnExcluir.setVisible(false);
            
            desabilitaCampos();

            //System.out.println(servicos.getData());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Valor inválido para Número ou Valor!/Preencha todos os campos!", "Atenção", JOptionPane.ERROR_MESSAGE);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Informe a Data do Serviço!", "Atenção", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

