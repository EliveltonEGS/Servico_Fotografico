    //Carrega Serviços cadastrados
    public void listaServicos(String nome) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("NÚMERO");
        modelo.addColumn("COD. CLIENTE");
        modelo.addColumn("CLIENTE");
        modelo.addColumn("SERVIÇO");
        modelo.addColumn("VALOR");
        modelo.addColumn("DATA");

        sdao = new ServicosDAO();

        for (Servicos s : sdao.listLimit(nome)) {

            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            String dataFormatada = formato.format(s.getData());

            modelo.addRow(new Object[]{s.getCodigo(), s.getNumero(), s.getClientes().getCodigo(),
                s.getClientes().getNome(), s.getDescricao(), Double.toString(s.getValor()).replace(".", ","), dataFormatada});
        }

        tbServicos.setModel(modelo);
    }
    
