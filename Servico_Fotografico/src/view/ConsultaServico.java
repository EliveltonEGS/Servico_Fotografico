    //Carrega tabela consulta de serviços
    public void listaServicos() throws ParseException {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("NUMERO");
        modelo.addColumn("CPF");
        modelo.addColumn("NOME");
        modelo.addColumn("SERVIÇO");
        modelo.addColumn("VALOR");
        modelo.addColumn("DATA");

        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        Date data_inicial = formato.parse(txtDataInicial.getText());
        Date data_final = formato.parse(txtDataFinal.getText());

        sdao = new ServicosDAO();

        for (Servicos s : sdao.consultaEntreDatas(data_inicial, data_final)) {

            Double valor = new Double(s.getValor() );
            NumberFormat nf = NumberFormat.getCurrencyInstance();
            String formatado = nf.format(valor);
    //O resultado é R$ 12.000.000,12
    //Double.toString(s.getValor()).replace(".", ",")
    
            String dataFormatada = formato.format(s.getData());
            modelo.addRow(new Object[]{s.getNumero(), s.getClientes().getCpf(), s.getClientes().getNome(), s.getDescricao(),
                formatado, dataFormatada});
        }

        tbConsultaServicos.setModel(modelo);
    }

