    private void txtGerarRelatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtGerarRelatorioActionPerformed
        try {
            Connection con = ConnectionFactory.conectar();

            HashMap parametros = new HashMap();

            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            Date data_inicial = formato.parse(txtDataInicial.getText());
            Date data_final = formato.parse(txtDataFinal.getText());

            parametros.put("data_inicial", data_inicial);
            parametros.put("data_final", data_final);

            String caminho = "C:/relatorios/relatorio.jasper";
            //String caminho = "C:/Users/Elivelton Silva/Documents/NetBeansProjects/Rodrigo_Foto/src/relatorio_temp/relatorio.jasper";
            try {
                JasperPrint print = JasperFillManager.fillReport(caminho, parametros, con);

                JasperViewer jv = new JasperViewer(print, false);
                jv.setVisible(true); //chama o formulario para visualização
                jv.toFront(); //set o formulario a frente da aplicação
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro ao chamar o relatório!\nErro:" + ex);
                ex.printStackTrace();
            }

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!" + ex.getMessage(), "Atenção", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtGerarRelatorioActionPerformed


